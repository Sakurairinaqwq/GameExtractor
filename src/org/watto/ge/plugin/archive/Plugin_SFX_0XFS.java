  @Override
  public int getMatchRating(FileManipulator fm) {
    try {

      int rating = 0;

      if (FieldValidator.checkExtension(fm, extensions)) {
        rating += 25;
      }

      // Header
      if (fm.readString(4).equals("0XFS")) {
        rating += 50;
      }

      if (fm.readInt() == 1) {
        rating += 5;
      }

      if (fm.readInt() == 28) {
        rating += 5;
      }

      long arcSize = fm.getLength();

      // Directory Offset
      if (FieldValidator.checkOffset(fm.readInt(), arcSize)) {
        rating += 5;
      }

      // Directory Length
      if (FieldValidator.checkLength(fm.readInt(), arcSize)) {
        rating += 5;
      }

      return rating;

    }
    catch (Throwable t) {
      return 0;
    }
  }

  /**
   **********************************************************************************************
   * Reads an [archive] File into the Resources
   **********************************************************************************************
   **/
  @Override
  public Resource[] read(File path) {
    try {

      // NOTE - Compressed files MUST know their DECOMPRESSED LENGTH
      //      - Uncompressed files MUST know their LENGTH

      addFileTypes();

      //ExporterPlugin exporter = Exporter_ZLib.getInstance();

      // RESETTING GLOBAL VARIABLES

      FileManipulator fm = new FileManipulator(path, false);

      long arcSize = fm.getLength();

      // 4 - Header (0XFS)
      // 4 - Unknown (1)
      fm.skip(8);

      // 4 - Header Length (28)
      int dirOffset = fm.readInt();
      FieldValidator.checkOffset(dirOffset, arcSize);

      // 4 - Filename Directory Offset [+28]
      fm.skip(4);

      // 4 - Details Directory Length (numFiles = DetailsDirectoryLength/12)
      int numFiles = fm.readInt() / 12;
      FieldValidator.checkNumFiles(numFiles);

      // 4 - Filename Directory Offset
      int filenameDirOffset = fm.readInt();
      FieldValidator.checkOffset(filenameDirOffset, arcSize);

      // 4 - Filename Directory Length
      fm.seek(filenameDirOffset);

      HashMap<Integer, String> names = new HashMap<Integer, String>(numFiles);
      for (int i = 0; i < numFiles; i++) {
        // 2 - File ID
        int fileID = fm.readShort();

        // X - Code String
        // 1 - null Code String Terminator
        String code = fm.readNullString();

        // X - Text String
        // 1 - null Text String Terminator
        String value = fm.readNullString();

        names.put(fileID, code + "_(" + value + ")");
      }

      fm.seek(dirOffset);

      Resource[] resources = new Resource[numFiles];
      TaskProgressManager.setMaximum(numFiles);

      // Loop through directory
      for (int i = 0; i < numFiles; i++) {
        // 4 - File Offset
        int offset = fm.readInt();
        FieldValidator.checkOffset(offset, arcSize);

        // 4 - File Length
        int length = fm.readInt();
        FieldValidator.checkLength(length, arcSize);

        // 4 - File ID
        int fileID = fm.readInt();

        String filename = names.get(fileID);
        if (filename == null) {
          filename = Resource.generateFilename(i);
        }
        filename += ".wav";

        //path,name,offset,length,decompLength,exporter
        Resource_WAV_RawAudio resource = new Resource_WAV_RawAudio(path, filename, offset, length);
        resource.setAudioProperties(11025, 8, 1, true);
        resources[i] = resource;

        TaskProgressManager.setValue(i);
      }

      fm.close();

      return resources;

    }
    catch (Throwable t) {
      logError(t);
      return null;
    }
  }

